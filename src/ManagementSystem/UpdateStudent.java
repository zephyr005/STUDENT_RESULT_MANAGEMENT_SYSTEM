    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM result WHERE rollNo='"+rollNo+"'");
         
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "RollNo doesn't exist");
            }
            else{
                jTextField4.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField6.setText(rs.getString(5));
                jTextField5.setText(rs.getString(6));
                jTextField1.setEditable(false);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_searchActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        int physicsMarks = Integer.parseInt(jTextField4.getText());
        int mathMarks = Integer.parseInt(jTextField2.getText());
        int chemistryMarks = Integer.parseInt(jTextField3.getText());
        int electricalMarks = Integer.parseInt(jTextField6.getText());
        int biologyMarks = Integer.parseInt(jTextField5.getText());
        int result = 0;
        result = physicsMarks + mathMarks + chemistryMarks + electricalMarks + biologyMarks;
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE result SET physics="+ physicsMarks+", math="+mathMarks+", chemistry="+chemistryMarks+", electrical="+electricalMarks+", biology="+biologyMarks+", result="+result+ " WHERE rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            UpdateStudent frame = new UpdateStudent();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_updateActionPerformed

